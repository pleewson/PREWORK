public class loops3 {
    public static void main(String[] args){

        for (int i=0; i < 6; i++) {
            System.out.println("Java");
        }
        System.out.println();

        int j = 0;
        while ( j < 6 ) {
            System.out.println("Java");
            j++;

            System.out.println();
        }
    }
}
