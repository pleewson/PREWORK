public class loops4 {
    public static void main(String[] args){

        for(int i = 1; i < 11; i++){
            System.out.print(i + " ");
        }

        System.out.println();

        int j = 1;
        while(j < 11){
            System.out.print(j + " ");
            j++;
        }
    }
}
